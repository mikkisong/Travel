package com.itwill.domain;

import java.util.List;

public class DataTableBean {
	private int draw; // 요청하는 draw와 같은 의미입니다. count
    private int recordsTotal; //필터링 전 전체 레코드 수
    private int recordsFiltered; //필터링 후 전체 레코드 수
    private List data; //테이블에 그릴 데이터 (array)
    private String error; //error 발생 시 표시할 메시지 (optional)
    
	public int getDraw() {
		return draw;
	}
	public void setDraw(int draw) {
		this.draw = draw;
	}
	public int getRecordsTotal() {
		return recordsTotal;
	}
	public void setRecordsTotal(int recordsTotal) {
		this.recordsTotal = recordsTotal;
	}
	public int getRecordsFiltered() {
		return recordsFiltered;
	}
	public void setRecordsFiltered(int recordsFiltered) {
		this.recordsFiltered = recordsFiltered;
	}
	public List getData() {
		return data;
	}
	public void setData(List data) {
		this.data = data;
	}
	public String getError() {
		return error;
	}
	public void setError(String error) {
		this.error = error;
	}
}
